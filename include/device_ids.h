#ifndef DEVICE_IDS_H
#define DEVICE_IDS_H

// VID/PID pair for a device in Nokia DLOAD mode
#define NOKIA_VID 0x0421
#define DLOAD_PID 0x05EE

// VID/PID pair from the QCOM bootloader commonly installed
#define QUALCOMM_VID 0x05C6
#define QCBL_PID 0x9006

// VID/PID pairs for the various Windows Mobile modes
#define MICROSOFT_VID 0x045E
#define WM7_MAINOS_PID 0x04EC
#define WM7_FFU_PID 0x0710 // FFU/SLDR, also Zune
#define WM7_BLDR_PID 0x041E

#endif // DEVICE_IDS_H
